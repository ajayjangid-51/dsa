    long long sum = 0;
    long long quotient = 0;
    for (int i = 31; i >= 0; i--)
    {
        long long t = sum + (divisor << i);
        if (t <= dividend)
        {
            deb(t);

            sum = sum + (divisor << i);
            quotient = quotient | (1LL << i);
        }
    }
