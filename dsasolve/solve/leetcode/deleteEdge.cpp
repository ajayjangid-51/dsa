int dfs(int node, vi &A, vii &adj, int &GraphSum, int &maxi, vb &visited)
{
    deb(node);
    int sum = A[node - 1];
    visited[node] = 1;
    for (auto x : adj[node])
    {
        deb(x);
        if (!visited[x])
            sum += dfs(x, A, adj, GraphSum, maxi, visited);
    }
    int diffproduct = ((((GraphSum - sum) % MOD) * sum) % MOD);
    deb(diffproduct);

    if (maxi < diffproduct)
    {
        maxi = diffproduct;
    }
    return sum;
}
