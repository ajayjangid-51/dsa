int createlps(string pat)
{
    int len = 0;
    int M = pat.size();
    vector<int> lps(M);

    lps[0] = 0; // lps[0] is always 0

    // the loop calculates lps[i] for i = 1 to M-1
    int i = 1;
    while (i < M)
    {
        if (pat[i] == pat[len])
        {
            len++;
            lps[i] = len;
            i++;
        }
        else // (pat[i] != pat[len])
        {
            // This is tricky. Consider the example.
            // AAACAAAA and i = 7. The idea is similar
            // to search step.
            if (len != 0)
            {
                len = lps[len - 1];

                // Also, note that we do not increment
                // i here
            }
            else // if (len == 0)
            {
                lps[i] = 0;
                i++;
            }
        }
    }
    return lps[M - 1];
}
int solve(string A)
{
    int n = A.size();
    string s(A.rbegin(), A.rend());
    s += ("$" + A);
    return (n - createlps(s));
}

