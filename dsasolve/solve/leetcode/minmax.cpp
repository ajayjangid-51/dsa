pii fn(int i, int j, vi &v)
{
    if (i == j)
    {
        pii p = {v[i], v[j]};
        return p;
    }
    int mid = (j + i) / 2;
    pii p1 = fn(i, mid, v);
    pii p2 = fn(mid + 1, j, v);
    pii p3;
    p3.first = max(p1.first, p2.first);
    p3.second = min(p1.second, p2.second);
    return p3;
}
